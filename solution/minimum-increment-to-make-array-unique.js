var minIncrementForUnique = function (A) {
  A.sort((a, b) => a - b);

  let min = Math.min(...A),
    n = A.length,
    larger = min + n - 1,
    handles = 0,
    start = min;

  for (let i = 0; i < n; i++) {
    let c = A[i];
    if (c <= start) {
      handles += (start - c);
      start++;
    } else {
      start = c + 1;
    }
  }

  return handles;
};